package com.yihuacomputer.fish.api.version;

public class VersionDistribute {
	private long versionTypeId;
	private long versionId;
	private String versionNo;
	private int versionNoNumber;
	public long getVersionTypeId() {
		return versionTypeId;
	}
	public void setVersionTypeId(long versionTypeId) {
		this.versionTypeId = versionTypeId;
	}
	public long getVersionId() {
		return versionId;
	}
	public void setVersionId(long versionId) {
		this.versionId = versionId;
	}
	public String getVersionNo() {
		return versionNo;
	}
	public void setVersionNo(String versionNo) {
		this.versionNo = versionNo;
	}
	public int getVersionNoNumber() {
		return versionNoNumber;
	}
	public void setVersionNoNumber(int versionNoNumber) {
		this.versionNoNumber = versionNoNumber;
	}
}
